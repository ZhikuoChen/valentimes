def mat2ascii(mat):
  '''
  Visualize a 2D numerical array as ASCII with greyscale -> char mapping
  '''
  # mapping courtesy Ben Fry (Processing.org)
  ink2ascii = ' .`-_\':,;^=+/"|)\\<>)iv%xclrs{*}I?!][1taeo7zjLunT#JCwfy' \
              '325Fp6mqSghVd4EgXPGZbYkOA&8U$@KHDBWNMR0Q'

  # scale matrix to 0-1
  mat = mat.astype(float)
  mat -= max(mat.min(), 0)
  mat /= mat.max()

  # map numbers into indices and assemble text
  ascii_mat = []
  idx_mat = np.ceil(mat * (len(ink2ascii) - 1)).astype(int)
  for r in range(idx_mat.shape[0]):
      ascii_row = ''.join([ink2ascii[x] for x in idx_mat[r,:].tolist()])
      ascii_mat.append(ascii_row)
  ascii = '\n'.join(ascii_mat) + '\n'
  return ascii

